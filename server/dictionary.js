export const getRandomWord = (category = null) => {
    let pool = [];

    if (category && DICTIONARY[category]) {
        // Use specific category
        pool = DICTIONARY[category];
    } else {
        // Use all categories (flatten the object values)
        Object.values(DICTIONARY).forEach(catWords => {
            pool = pool.concat(catWords);
        });
    }

    if (pool.length === 0) {
        // Fallback
        return { word: "Error", related: "Bug" };
    }

    const randomIndex = Math.floor(Math.random() * pool.length);
    return pool[randomIndex];
};
